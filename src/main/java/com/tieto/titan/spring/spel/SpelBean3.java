package com.tieto.titan.spring.spel;

public class SpelBean3 {
	private double multiplier;
	private double randomNumber;
	private double adjustendAmount;
	private double average;
	private double remainder;
	private double area;
	private String fullName;
	
	public double getMultiplier() {
		return multiplier;
	}
	
	

	public void setMultiplier(double multiplier) {
		this.multiplier = multiplier;
	}

	public double getRandomNumber() {
		return randomNumber;
	}

	public void setRandomNumber(double randomNumber) {
		this.randomNumber = randomNumber;
	}



	public double getAdjustendAmount() {
		return adjustendAmount;
	}



	public void setAdjustendAmount(double adjustendAmount) {
		this.adjustendAmount = adjustendAmount;
	}



	public double getAverage() {
		return average;
	}



	public void setAverage(double average) {
		this.average = average;
	}



	public double getRemainder() {
		return remainder;
	}



	public void setRemainder(double remainder) {
		this.remainder = remainder;
	}



	public double getArea() {
		return area;
	}



	public void setArea(double area) {
		this.area = area;
	}



	public String getFullName() {
		return fullName;
	}



	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	
}
